// qualifiedTypeName returns the Go type expression for a message type referenced
// in the generated file. When the type is from the same proto package as the file
// being generated, it uses the pre-declared pkgAlias to avoid adding a duplicate
// import alongside the one in generateImports. For cross-package types (e.g.,
// emptypb.Empty for google.protobuf.Empty), it delegates to g.QualifiedGoIdent
// which registers the import automatically (protogen prepends a separate import
// block for these — valid Go, just two import blocks in the file).
//
// Known limitation: protogen's automatic import resolution does not see names
// claimed by the manual import block (`pkgAlias`, `connectaip`, `connect`,
// `connectsse`). If a generated file's local pkgAlias literally matches a
// cross-package alias protogen would assign — e.g., a proto with
// `option go_package = "..../emptypb;emptypb"` returning `google.protobuf.Empty`
// — the file ends up with two `emptypb` imports and won't compile. Avoiding
// this would require switching the file to use protogen-managed imports for
// everything (no manual `import (...)` block); deferred to a follow-up.
func qualifiedTypeName(g *protogen.GeneratedFile, ident protogen.GoIdent, pkgAlias, protoImportPath string) string {
	if string(ident.GoImportPath) == protoImportPath {
		return pkgAlias + "." + ident.GoName
	}
	return g.QualifiedGoIdent(ident)
}

// generateServiceClient generates the AIP client for a service.
func generateServiceClient(g *protogen.GeneratedFile, service *protogen.Service, pkgAlias, protoImportPath string) {
	var routes []routeInfo
	for _, method := range service.Methods {
		if method.Desc.IsStreamingClient() {
			continue
		}
		httpRule := getHTTPRule(method)
		if httpRule == nil {
			continue
		}
		route := parseHTTPRule(method, httpRule)
		if route == nil {
			continue
		}
		// Skip routes with non-string path parameters (e.g., bytes fields)
		// These can't be easily converted to URL path segments
		if !hasOnlyStringPathParams(route, method.Input) {
			continue
		}
		routes = append(routes, *route)
	}

	if len(routes) == 0 {
		return
	}

	serviceName := service.GoName
	clientName := serviceName + "AIPClient"
	// When every RPC on the service has a usable HTTP rule, the AIP client
	// is a complete implementation of the standard {Service}Client interface
	// emitted by protoc-gen-connect-go and we return that directly so callers
	// can use it as a drop-in replacement. We also emit `type {Service}AIPClient =
	// {Service}Client` so existing downstream code that types variables as
	// {Service}AIPClient keeps compiling after regeneration.
	//
	// When some RPCs are filtered out (gRPC-only, client-streaming, unsupported
	// path params), we fall back to emitting an AIP-specific interface so the
	// generated file still compiles — partial coverage cannot satisfy the
	// standard interface.
	allMethodsCovered := len(routes) == len(service.Methods)
	clientInterface := serviceName + "Client"
	if allMethodsCovered {
		g.P("// ", clientName, " is an alias for the standard ", clientInterface, " interface.")
		g.P("// All RPCs on ", serviceName, " have HTTP rules, so the AIP client is a")
		g.P("// drop-in replacement for ", clientInterface, "; this alias is kept for")
		g.P("// backwards compatibility with code that types variables as ", clientName, ".")
		g.P("type ", clientName, " = ", clientInterface)
		g.P()
	} else {
		clientInterface = clientName
		g.P("// ", clientName, " is an AIP client for ", serviceName, ".")
		g.P("// ", serviceName, " has RPCs without HTTP rules; use the connect-go-generated")
		g.P("// ", serviceName, "Client (via New", serviceName, "Client) for full coverage.")
		g.P("type ", clientName, " interface {")
		for _, route := range routes {
			inputType := qualifiedTypeName(g, route.inputType.GoIdent, pkgAlias, protoImportPath)
			outputType := qualifiedTypeName(g, route.outputType.GoIdent, pkgAlias, protoImportPath)
			if route.isServerStreaming {
				g.P("\t", route.rpcMethod, "(ctx context.Context, req *connect.Request[", inputType, "]) (*connect.ServerStreamForClient[", outputType, "], error)")
			} else {
				g.P("\t", route.rpcMethod, "(ctx context.Context, req *connect.Request[", inputType, "]) (*connect.Response[", outputType, "], error)")
			}
		}
		g.P("}")
		g.P()
	}

	implName := strings.ToLower(serviceName[:1]) + serviceName[1:] + "AIPClient"
	g.P("type ", implName, " struct {")
	for _, route := range routes {
		fieldName := strings.ToLower(route.rpcMethod[:1]) + route.rpcMethod[1:]
		inputType := qualifiedTypeName(g, route.inputType.GoIdent, pkgAlias, protoImportPath)
		outputType := qualifiedTypeName(g, route.outputType.GoIdent, pkgAlias, protoImportPath)
		if route.isServerStreaming {
			g.P("\t", fieldName, " *connect.Client[", inputType, ", ", outputType, "]")
		} else {
			g.P("\t", fieldName, " *connectaip.Client[", inputType, ", ", outputType, "]")
		}
	}
	g.P("}")
	g.P()

	// Generate constructor
	g.P("// New", clientName, " creates a new AIP client for ", serviceName, ".")
	if allMethodsCovered {
		g.P("// The returned client satisfies the standard ", clientInterface, " interface,")
		g.P("// so it can be used as a drop-in replacement for New", clientInterface, ".")
	}
	g.P("func New", clientName, "(httpClient connect.HTTPClient, baseURL string, opts ...connectaip.ClientOption) ", clientInterface, " {")
	g.P("\treturn &", implName, "{")
	for _, route := range routes {
		fieldName := strings.ToLower(route.rpcMethod[:1]) + route.rpcMethod[1:]
		procedureConst := serviceName + route.rpcMethod + "Procedure"

		if route.isServerStreaming {
			inputType := qualifiedTypeName(g, route.inputType.GoIdent, pkgAlias, protoImportPath)
			outputType := qualifiedTypeName(g, route.outputType.GoIdent, pkgAlias, protoImportPath)
			restPath := route.serveMuxPath + route.customVerb
			pathVarFnArg := "nil"
			if len(route.pathVars) > 0 {
				pathVarFnArg = serviceName + route.rpcMethod + "SSEPathVars"
			}
			g.P("\t\t", fieldName, ": connect.NewClient[", inputType, ", ", outputType, "](")
			g.P("\t\t\tconnectaip.NewSSEClient(httpClient, baseURL+\"", restPath, "\", ", pathVarFnArg, ", opts...),")
			g.P("\t\t\tconnectaip.SSEProcedureURL(baseURL, ", procedureConst, "),")
			g.P("\t\t\tappend([]connect.ClientOption{connect.WithProtoJSON()}, connectaip.ConnectClientOptions(opts...)...)...,")
			g.P("\t\t),")
		} else {
			inputType := qualifiedTypeName(g, route.inputType.GoIdent, pkgAlias, protoImportPath)
			outputType := qualifiedTypeName(g, route.outputType.GoIdent, pkgAlias, protoImportPath)

			// Build MethodSpec
			g.P("\t\t", fieldName, ": connectaip.NewClient[", inputType, ", ", outputType, "](")
			g.P("\t\t\thttpClient, baseURL,")
			g.P("\t\t\tconnectaip.MethodSpec{")
			g.P("\t\t\t\tHTTPMethod: \"", route.method, "\",")
			g.P("\t\t\t\tURLPattern: \"", route.serveMuxPath+route.customVerb, "\",")
			g.P("\t\t\t\tProcedure:  ", procedureConst, ",")
			if len(route.pathVars) > 0 {
				g.P("\t\t\t\tPathVars: []connectaip.PathVar{")
				for _, pv := range route.pathVars {
					placeholder := "{" + pv.name
					if pv.wildcard && strings.Contains(route.serveMuxPath, pv.name+"...") {
						placeholder += "..."
					}
					placeholder += "}"
					g.P("\t\t\t\t\t{Placeholder: \"", placeholder, "\", Prefix: \"", pv.prefix, "\"},")
				}
				g.P("\t\t\t\t},")
			}
			g.P("\t\t\t},")

			// Path var extractor function
			if len(route.pathVars) > 0 {
				g.P("\t\t\t", serviceName, route.rpcMethod, "PathVars,")
			} else {
				g.P("\t\t\tnil,")
			}

			// Query param extractor function (for GET/DELETE or empty body methods)
			if route.method == "GET" || route.method == "DELETE" || route.bodyField == "" {
				if hasQueryParams(route) {
					g.P("\t\t\t", serviceName, route.rpcMethod, "Query,")
				} else {
					g.P("\t\t\tfunc(*", inputType, ") map[string]string { return nil },")
				}
			} else {
				g.P("\t\t\tnil,")
			}

			g.P("\t\t\topts...,")
			g.P("\t\t),")
		}
	}
	g.P("\t}")
	g.P("}")
	g.P()

	// Generate path var extractor functions (unary only)
	for _, route := range routes {
		if !route.isServerStreaming && len(route.pathVars) > 0 {
			generatePathVarExtractor(g, serviceName, route, pkgAlias, protoImportPath)
		}
	}

	// Generate SSE path var extractor functions (streaming with path vars)
	for _, route := range routes {
		if route.isServerStreaming && len(route.pathVars) > 0 {
			generateSSEPathVarExtractor(g, serviceName, route)
		}
	}

	// Generate query param extractor functions (unary only)
	for _, route := range routes {
		if !route.isServerStreaming && (route.method == "GET" || route.method == "DELETE" || route.bodyField == "") && hasQueryParams(route) {
			generateQueryExtractor(g, serviceName, route, pkgAlias, protoImportPath)
		}
	}

	// Generate methods (one-line delegations)
	for _, route := range routes {
		generateClientMethod(g, implName, route, pkgAlias, protoImportPath)
	}
}

// generateClientMethod generates a single client method (one-line delegation).
// Method signatures match the standard {Service}Client interface from
// protoc-gen-connect-go so the AIP client is assignable to it.
func generateClientMethod(g *protogen.GeneratedFile, implName string, route routeInfo, pkgAlias, protoImportPath string) {
	fieldName := strings.ToLower(route.rpcMethod[:1]) + route.rpcMethod[1:]

	if route.isServerStreaming {
		inputType := qualifiedTypeName(g, route.inputType.GoIdent, pkgAlias, protoImportPath)
		outputType := qualifiedTypeName(g, route.outputType.GoIdent, pkgAlias, protoImportPath)
		g.P("func (c *", implName, ") ", route.rpcMethod, "(ctx context.Context, req *connect.Request[", inputType, "]) (*connect.ServerStreamForClient[", outputType, "], error) {")
		g.P("\treturn c.", fieldName, ".CallServerStream(ctx, req)")
		g.P("}")
		g.P()
		return
	}

	inputType := qualifiedTypeName(g, route.inputType.GoIdent, pkgAlias, protoImportPath)
	outputType := qualifiedTypeName(g, route.outputType.GoIdent, pkgAlias, protoImportPath)

	g.P("func (c *", implName, ") ", route.rpcMethod, "(ctx context.Context, req *connect.Request[", inputType, "]) (*connect.Response[", outputType, "], error) {")
	g.P("\treturn c.", fieldName, ".CallRequest(ctx, req)")
	g.P("}")
	g.P()
}

// generatePathVarExtractor generates a function to extract path variables from a request.
func generatePathVarExtractor(g *protogen.GeneratedFile, serviceName string, route routeInfo, pkgAlias, protoImportPath string) {
	inputType := qualifiedTypeName(g, route.inputType.GoIdent, pkgAlias, protoImportPath)
	funcName := serviceName + route.rpcMethod + "PathVars"

	g.P("func ", funcName, "(req *", inputType, ") map[string]string {")
	g.P("\treturn map[string]string{")
	for _, pv := range route.pathVars {
		placeholder := "{" + pv.name
		if pv.wildcard && strings.Contains(route.serveMuxPath, pv.name+"...") {
			placeholder += "..."
		}
		placeholder += "}"

		if pv.multiGroup != nil {
			// Multi-wildcard: extract each sub-var from the full field value by splitting.
			// E.g., for name = "agents/foo/slos/bar" with prefix "agents/" and sep "/slos/":
			//   name_0 → connectaip.SplitMultiWildcard(req.GetName(), "agents/", "/slos/", 0)
			//   name_1 → connectaip.SplitMultiWildcard(req.GetName(), "agents/", "/slos/", 1)
			// Using connectaip.SplitMultiWildcard avoids index-out-of-range panics on malformed names.
			mg := pv.multiGroup
			fieldAccessor := protoFieldAccessor(mg.fieldPath)

			if len(mg.names) == 2 {
				// Two-wildcard case: use connectaip.SplitMultiWildcard for safe (non-panicking) access.
				sep := mg.seps[0]
				g.P("\t\t\"", placeholder, "\": connectaip.SplitMultiWildcard(", fieldAccessor, ", ", fmt.Sprintf("%q", mg.prefix), ", ", fmt.Sprintf("%q", sep), ", ", mg.idx, "),")
			} else {
				// General N-wildcard case: chain SplitN calls. For now, use the full field
				// value for the first var and progressively strip prefixes for subsequent ones.
				// This covers the common 2-wildcard case; deeper nesting is uncommon.
				g.P("\t\t\"", placeholder, "\": ", fieldAccessor, ", // TODO: multi-segment split for index ", mg.idx)
			}
		} else {
			fieldAccessor := protoFieldAccessor(pv.fieldPath)
			g.P("\t\t\"", placeholder, "\": ", fieldAccessor, ",")
		}
	}
	g.P("\t}")
	g.P("}")
	g.P()
}

// generateSSEPathVarExtractor generates a function that extracts path variable values from
// a raw JSON message (as produced by connectsse.Client) for server-streaming routes.
// It handles both simple fields ("name") and nested fields ("session.name").
// The function signature is: func(msgJSON []byte) iter.Seq2[string, string].
func generateSSEPathVarExtractor(g *protogen.GeneratedFile, serviceName string, route routeInfo) {
	funcName := serviceName + route.rpcMethod + "SSEPathVars"
	g.P("func ", funcName, "(msgJSON []byte) iter.Seq2[string, string] {")
	g.P("\treturn func(yield func(string, string) bool) {")
	g.P("\t\tvar fields map[string]json.RawMessage")
	g.P("\t\t_ = json.Unmarshal(msgJSON, &fields)")
	for i, pv := range route.pathVars {
		jsonPath := jsonFieldPath(route.inputType, pv.fieldPath)
		placeholder := "{" + pv.name
		if pv.wildcard && strings.Contains(route.serveMuxPath, pv.name+"...") {
			placeholder += "..."
		}
		placeholder += "}"

		// Generate nested JSON access for each level of the dotted field path.
		// Intermediate levels unmarshal into map[string]json.RawMessage; the final
		// level unmarshals into a string.
		indent := "\t\t"
		currentMap := "fields"
		for depth, jsonName := range jsonPath {
			rawVar := fmt.Sprintf("raw%d_%d", i, depth)
			if depth < len(jsonPath)-1 {
				nestedVar := fmt.Sprintf("nested%d_%d", i, depth)
				g.P(indent, "if ", rawVar, ", ok := ", currentMap, "[\"", jsonName, "\"]; ok {")
				indent += "\t"
				g.P(indent, "var ", nestedVar, " map[string]json.RawMessage")
				g.P(indent, "_ = json.Unmarshal(", rawVar, ", &", nestedVar, ")")
				currentMap = nestedVar
			} else {
				g.P(indent, "if ", rawVar, ", ok := ", currentMap, "[\"", jsonName, "\"]; ok {")
				indent += "\t"
				g.P(indent, "var val string")
				g.P(indent, "_ = json.Unmarshal(", rawVar, ", &val)")
				if pv.prefix != "" {
					g.P(indent, "val = strings.TrimPrefix(val, \"", pv.prefix, "\")")
				}
				g.P(indent, "if val != \"\" {")
				g.P(indent, "\tif !yield(\"", placeholder, "\", val) {")
				g.P(indent, "\t\treturn")
				g.P(indent, "\t}")
				g.P(indent, "}")
			}
		}
		for range jsonPath {
			indent = indent[:len(indent)-1]
			g.P(indent, "}")
		}
	}
	g.P("\t}")
	g.P("}")
	g.P()
}

// jsonFieldPath returns the sequence of protojson field names (camelCase) for a
// possibly dotted field path, traversing nested message types as needed.
// E.g. "session.name" on a message returns ["session", "name"] using JSON names.
func jsonFieldPath(msg *protogen.Message, fieldPath string) []string {
	parts := strings.Split(fieldPath, ".")
	result := make([]string, 0, len(parts))
	current := msg
	for _, part := range parts {
		if current == nil {
			result = append(result, part)
			continue
		}
		found := false
		for _, f := range current.Fields {
			if string(f.Desc.Name()) == part {
				result = append(result, f.Desc.JSONName())
				if f.Message != nil {
					current = f.Message
				} else {
					current = nil
				}
				found = true
				break
			}
		}
		if !found {
			result = append(result, part)
			current = nil
		}
	}
	return result
}

// generateQueryExtractor generates a function to extract query parameters from a request.
func generateQueryExtractor(g *protogen.GeneratedFile, serviceName string, route routeInfo, pkgAlias, protoImportPath string) {
	inputType := qualifiedTypeName(g, route.inputType.GoIdent, pkgAlias, protoImportPath)
	funcName := serviceName + route.rpcMethod + "Query"

	// Create a set of path variable field paths for quick lookup
	pathVarFields := make(map[string]bool)
	for _, pv := range route.pathVars {
		pathVarFields[pv.fieldPath] = true
	}

	g.P("func ", funcName, "(req *", inputType, ") map[string]string {")
	g.P("\tresult := make(map[string]string)")

	for _, field := range route.inputType.Fields {
		fieldPath := string(field.Desc.Name())
		if pathVarFields[fieldPath] {
			continue
		}

		fieldAccessor := "req.Get" + field.GoName + "()"
		jsonName := field.Desc.JSONName()

		switch field.Desc.Kind().String() {
		case "string":
			g.P("\tif v := ", fieldAccessor, "; v != \"\" {")
			g.P("\t\tresult[\"", jsonName, "\"] = v")
			g.P("\t}")
		case "int32", "int64", "uint32", "uint64":
			g.P("\tif v := ", fieldAccessor, "; v != 0 {")
			g.P("\t\tresult[\"", jsonName, "\"] = fmt.Sprint(v)")
			g.P("\t}")
		case "float", "double":
			g.P("\tif v := ", fieldAccessor, "; v != 0 {")
			g.P("\t\tresult[\"", jsonName, "\"] = fmt.Sprint(v)")
			g.P("\t}")
		case "bool":
			g.P("\tif v := ", fieldAccessor, "; v {")
			g.P("\t\tresult[\"", jsonName, "\"] = \"true\"")
			g.P("\t}")
		case "enum":
			g.P("\tif v := ", fieldAccessor, "; v != 0 {")
			g.P("\t\tresult[\"", jsonName, "\"] = fmt.Sprint(int32(v))")
			g.P("\t}")
		}
	}

	g.P("\treturn result")
	g.P("}")
	g.P()
}

// hasQueryParams checks if a route has any query parameters to extract.
func hasQueryParams(route routeInfo) bool {
	pathVarFields := make(map[string]bool)
	for _, pv := range route.pathVars {
		pathVarFields[pv.fieldPath] = true
	}

	for _, field := range route.inputType.Fields {
		fieldPath := string(field.Desc.Name())
		if pathVarFields[fieldPath] {
			continue
		}
		switch field.Desc.Kind().String() {
		case "string", "int32", "int64", "uint32", "uint64", "bool", "float", "double", "enum":
			return true
		}
	}
	return false
}

// hasOnlyStringPathParams checks if all path parameters in a route are string type.
// Returns false if any path parameter is a non-string type (e.g., bytes).
func hasOnlyStringPathParams(route *routeInfo, inputMsg *protogen.Message) bool {
	for _, pv := range route.pathVars {
		field := findFieldByPath(pv.fieldPath, inputMsg)
		if field == nil {
			continue
		}
		// Only allow string fields as path parameters
		if field.Desc.Kind().String() != "string" {
			return false
		}
	}
	return true
}

// findFieldByPath finds a field in a message by its dot-separated path.
// Example: "customer.name" finds the "name" field in the nested "customer" message.
func findFieldByPath(fieldPath string, msg *protogen.Message) *protogen.Field {
	parts := strings.Split(fieldPath, ".")
	currentMsg := msg

	for i, part := range parts {
		var found *protogen.Field
		for _, f := range currentMsg.Fields {
			if string(f.Desc.Name()) == part {
				found = f
				break
			}
		}
		if found == nil {
			return nil
		}
		if i == len(parts)-1 {
			return found
		}
		// Navigate into nested message
		if found.Message == nil {
			return nil
		}
		currentMsg = found.Message
	}
	return nil
}

// protoFieldAccessor returns the Go code to access a proto field given its path.
// Example: "customer.name" -> "req.GetCustomer().GetName()"
func protoFieldAccessor(fieldPath string) string {
	parts := strings.Split(fieldPath, ".")
	var sb strings.Builder
	sb.WriteString("req")
	for _, part := range parts {
		sb.WriteString(".Get")
		sb.WriteString(snakeToPascal(part))
		sb.WriteString("()")
	}
	return sb.String()
}

// snakeToPascal converts a snake_case string to PascalCase.
// Example: "customer_name" -> "CustomerName"
func snakeToPascal(s string) string {
	parts := strings.Split(s, "_")
	for i := range parts {
		if len(parts[i]) > 0 {
			parts[i] = strings.ToUpper(parts[i][:1]) + parts[i][1:]
		}
	}
	return strings.Join(parts, "")
}
